export function detectLanguage(locale: string | null | undefined): Language {
  return locale?.toLowerCase().startsWith("fi") ? "fi" : "en";
}

export function translate(
  language: Language,
  key: string,
  values: TranslationValues = {},
) {
  const template = language === "fi" ? FINNISH_TRANSLATIONS[key] ?? key : key;
  return template.replace(/\{(\w+)\}/g, (match, name: string) =>
    values[name] === undefined ? match : String(values[name]));
}

export function translateError(
  language: Language,
  error: unknown,
  fallback: string,
) {
  if (error instanceof Error && language === "en") return error.message;
  if (error instanceof Error && FINNISH_TRANSLATIONS[error.message]) {
    return translate(language, error.message);
  }
  return translate(language, fallback);
}
